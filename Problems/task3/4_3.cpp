///////////////////////////////////////////////////////////////////////////////////////////////

#include <cassert>
#include <vector>
#include <iostream>


///////////////////////////////////////////////////////////////////////////////////////////////
// Добавление всех аргументов типа int в контейнер с push_back
///////////////////////////////////////////////////////////////////////////////////////////////

template <typename Container, typename... Args>
void add_all(Container &container, Args... args)
{
    (container.push_back(args), ...);
}

///////////////////////////////////////////////////////////////////////////////////////////////
// ТЕСТЫ
///////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    // test 1: обычный случай
    {
        std::vector<int> v;
        add_all(v, 1, 2, 3, 4, 5);

        assert((v == std::vector<int>{1, 2, 3, 4, 5}));
    }

    // test 2: один элемент
    {
        std::vector<int> v;
        add_all(v, 42);

        assert(v.size() == 1);
        assert(v[0] == 42);
    }

    // test 3: много элементов
    {
        std::vector<int> v;
        add_all(v, 10, 20, 30, 40, 50, 60);

        assert(v.size() == 6);
        assert(v[5] == 60);
    }

    // test 4: несколько вызовов
    {
        std::vector<int> v;
        add_all(v, 1, 2);
        add_all(v, 3, 4);

        assert((v == std::vector<int>{1, 2, 3, 4}));
    }

    std::cout << "All generic tests passed!\n";

    return 0;
}

